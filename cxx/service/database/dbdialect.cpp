#include <QSqlQuery>
#include <QSqlDriver>
#include <QRegularExpression>
#include "dbdialect.h"
#include "sql.h"
#include <QDecNumber.hh>

#define CLASS_NAME "dbDialect"

#define PG_IGNORE_DUPLICATE(createSql) \
    "DO $$ begin\n" createSql ";\n" \
    "exception when duplicate_object then raise notice '%, skipping', sqlerrm using errcode = sqlstate;\n" \
    "end $$)"

static const QHash<const QString, const char*> createSchemaQuery{
    {MYSQL_DRIVER, "create database if not exists :schema"},
    {PG_DRIVER, PG_IGNORE_DUPLICATE("create database :schema")},
};

static const QHash<const QString, const char*> createUserQuery{
    {MYSQL_DRIVER, "create user if not exists :user identified by :password"},
    {PG_DRIVER, PG_IGNORE_DUPLICATE("create user :user with password :password")},
};

static const QHash<const QString, const char*> grantUserQuery{
    {MYSQL_DRIVER, "grant all on %1.* to :user"},
    {PG_DRIVER, "grant all on database %1 to :user"},
};

namespace dbDialect {
    static const auto pgIdType = "bigint not null primary key generated by default as identity";
    static const auto sqliteIdType = "integer not null primary key";
    static const auto mysqlIdType = "bigint not null primary key auto_increment";

    QString inList(const QSqlDatabase &db, const char *column, const char *placeholder) {
        if (IS_SQLITE(db)) return QString("%0 in (select value from json_each(%1))").arg(column, placeholder);
        else if (IS_PG(db)) return QString("to_jsonb(%0) <@ %1 ").arg(column, placeholder);
        else return QString("%0 member of (%1)").arg(column, placeholder);
    }

    QSqlQuery prepareGetByIds(const QSqlDatabase &db, const char *getAllSql, const QList<domain_id> ids, const char *idColumn) {
        QSqlQuery query{db};
        QString sql = getAllSql;
        sql += "\nwhere " + inList(db, idColumn, ":ids");
        query.prepare(sql);
        sql::bindList(query, ":ids", ids);
        return query;
    }

    void createSchema(const QSqlDatabase &db, const QString &schema) {
        const char* sql = createSchemaQuery.value(db.driverName());
        if (sql) {
            QSqlQuery query{sql};
            sql::bindValue(query, ":schema", schema);
            sql::exec(query, CLASS_NAME, "createSchema");
        }
    }

    void addUser(const QSqlDatabase &db, const QString &schema, const QString &user, const QString &password) {
        const char* sql = createUserQuery.value(db.driverName());
        if (sql) {
            QSqlQuery addQuery{sql};
            sql::bindValue(addQuery, ":user", user);
            sql::bindValue(addQuery, ":password", password);
            sql::exec(addQuery, CLASS_NAME, "addUser");
            QSqlQuery grantQuery{QString{grantUserQuery.value(db.driverName())}.arg(schema)};
            sql::bindValue(grantQuery, ":user", user);
            sql::exec(grantQuery, CLASS_NAME, "grantUser");
        }
    }
}